public enum CounterType {

    /**
     * Using this CounterType will format the CounterView text as a comma separated integer.
     */
    NUMBER,
    /**
     * Using this CounterType will format the CounterView text as a decimal to 2vp.
     */
    DECIMAL,
    /**
     * Using this CounterType will format the CounterView text as a comma separated decimal to 2vp.
     */
    BOTH
}
